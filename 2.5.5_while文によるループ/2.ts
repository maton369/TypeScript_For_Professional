// 1から100までの合計を求める（breakでループを抜けるパターン）
let sum = 0;     // 合計を格納する変数
let i = 1;       // カウンタ変数

while (true) {   // 無限ループ（終了条件は内部で制御する）
    if (i > 100) { // i が 100 を超えたらループ終了
        break;       // while ループを強制的に抜ける
    }
    sum += i;      // 現在の i を sum に加算
    i++;           // i を 1 増やす
}

console.log(sum); // 5050 と表示される