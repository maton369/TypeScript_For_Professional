// 変数 num を初期化（初期値は 0）
let num = 0;

// num に 100 を加算（0 + 100）
num += 100; // num は 100

// num に 4 を掛け算（100 * 4）
num *= 4; // num は 400

// num から 200 を減算（400 - 200）
num -= 200; // num は 200

// num を 2 で割る（200 / 2）
num /= 2; // num は 100

// num の平方根を計算（100 の 0.5 乗 = √100）
num **= 0.5; // num は 10

// 最終的な num の値を表示（10 と出力される）
console.log(num); // 10