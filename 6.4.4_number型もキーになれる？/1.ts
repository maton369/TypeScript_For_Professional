type Obj = {
    0: string;  // 数値リテラルキー（型としては "0" ではなく数値 0）
    1: number;  // 数値リテラルキー（型としては 1）
}

const obj: Obj = {
    0: "uhyo",   // 数値リテラルキーとして代入
    "1": 26      // プロパティ記述時は "1" でも同じ意味になる
};

obj["0"] = "john"; // 文字列 "0" でアクセスしても OK（JavaScript の仕様で数値キーは文字列に変換される）
obj[1] = 15;       // 数値 1 でアクセスしても OK

// keyof Obj の計算結果は 0 | 1 型になる
//   - TypeScript ではオブジェクトの数値リテラルキーは、そのまま数値リテラル型として扱われる
type ObjKeys = keyof Obj; // 0 | 1