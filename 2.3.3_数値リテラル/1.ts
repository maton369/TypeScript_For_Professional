// `binary` は 2進数リテラル `0b1010` を使って 10 を表している。
// `0b` は「バイナリ（binary）」を意味するプレフィックスで、後続の数値を2進数として解釈する。
const binary = 0b1010; // = 1×8 + 0×4 + 1×2 + 0×1 = 10（10進数）

// `octal` は 8進数リテラル `0o755` を使って 493 を表している。
// `0o` は「オクタル（octal）」を意味するプレフィックスで、後続の数値を8進数として解釈する。
const octal = 0o755; // = 7×64 + 5×8 + 5 = 448 + 40 + 5 = 493（10進数）

// `hexadecimal` は 16進数リテラル `0xff` を使って 255 を表している。
// `0x` は「ヘキサ（hexadecimal）」を意味するプレフィックスで、後続の数値を16進数として解釈する。
const hexadecimal = 0xff; // = 15×16 + 15 = 240 + 15 = 255（10進数）

// それぞれの値をコンソールに出力する。
// 出力: 10 493 255
console.log(binary, octal, hexadecimal);