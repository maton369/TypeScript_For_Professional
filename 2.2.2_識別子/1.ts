// `あいう` は数値 123 を保持する定数である。
// TypeScript は日本語を含む Unicode の識別子を変数名として許容しているため、問題なく使用できる。
const あいう = 123;

// `技術評論社` は `あいう` に 876 を加算した値（999）を保持する定数である。
// こちらも型注釈は省略されているが、加算演算により `number` 型と推論される。
const 技術評論社 = あいう + 876;

// `技術評論社` の値をコンソールに出力する。
// 出力結果は 999 となる。
console.log(技術評論社); // 999 と表示される