// オブジェクト `obj` を定義する。プロパティとして数値 `foo` と文字列 `bar` を持つ
const obj = {
    foo: 123,                  // 数値型のプロパティ
    bar: "Hello, world!",      // 文字列型のプロパティ
};

// プロパティ `foo` の値を出力する。123 が表示される
console.log(obj.foo); // 123 と表示される

// プロパティ `bar` の値を出力する。"Hello, world!" が表示される
console.log(obj.bar); // "Hello, world!" と表示される